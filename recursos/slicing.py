s = 'python'
l = list(s)


# A sintaxe s [: 3] se chamafatiamento (slicing) e está implementada para muitos tipos de sequências Python.
# se aplicada depois do : pega apenas o valor indicado de letras
# se aplicado antes do : ele exclui do inicio o valor indicado de letras

print(l[:3])  # ['p', 'y', 't']
print(l[2:])  # ['t', 'h', 'o', 'n']
